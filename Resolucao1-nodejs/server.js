let INDICE = 13, SOMA = 0, K = 0;

while (K < INDICE) {
  K = K + 1;
  SOMA = SOMA + K;
}

console.log(SOMA);

// Aqui nos indicamos que nosso indice começara no valor 13, e que a soma começara no valor 0, e que K começara no valor 0.
// Enquanto K for menor que INDICE, K ira receber ele mesmo mais 1, e SOMA ira receber ela mesma mais K.
// No final, ira aparecer no console o valor da soma, que é 91.